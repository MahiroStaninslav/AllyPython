M = int(input('Quantos metros você quer? '))
CM = M * 100
MM = M * 1000
print('Seu valor em {} Metros. \n Seu valor em {} Centímetros. (Para calcular Metros para Centímetros, multiplique o valor do comprimento por 100. Para Centímetros e Metros, divida o valor por 100!) \n Seu valor em {} Milímetros. (Para calcular Metros para milímetros, multiplique o valor do comprimento por 1000, o contrário, divida por 1000!)'.format(M, CM, MM))
