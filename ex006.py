n = int(input('Digite o número desejado: '))
dobro = n * 2
print('Seu dobro é: {} .'.format(dobro))
triplo = n * 3
print('Seu Triplo é: {} .'.format(triplo))
quadruplo = n * 4
print('Seu Quadruplo é: {} .'.format(quadruplo))
quintuplo = n * 5
print('Seu Quintuplo é: {} . '.format(quintuplo))
raizq = n ** (1/2)
print('Sua Raiz Quadrada é: {:.1f} .'.format(raizq))

# Para calcular a raiz quadrada, pode usar também outra forma (pow de potência, sendo o X a base) pow(X, (1/2))
